export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    const { type, tabId, message, history } = body;

    // ============================================
    // 🎯 섹션 설명 요청 처리 (OpenAI 호출 없이 고정 스크립트 반환)
    // ============================================
    if (type === "tab_explain") {
      const script = SECTION_SCRIPTS[tabId];

      if (script) {
        console.log(`📑 Section explain request: ${tabId}`);
        return new Response(JSON.stringify({ reply: script, action: "none", tabId: null }), {
          status: 200,
          headers: { "Content-Type": "application/json" },
        });
      } else {
        return new Response(JSON.stringify({ reply: "해당 섹션에 대한 설명을 찾을 수 없습니다.", action: "none", tabId: null }), {
          status: 200,
          headers: { "Content-Type": "application/json" },
        });
      }
    }

    // ============================================
    // 💬 일반 채팅 요청 처리 (OpenAI API 사용)
    // ============================================
    if (!process.env.OPENAI_API_KEY) {
      throw new Error("OpenAI API key is missing");
    }

    const messages: OpenAI.ChatCompletionMessageParam[] = [
      { role: "system", content: SYSTEM_PROMPT },
      ...(history || []).map((msg: { role: string; content: string }) => ({
        role: msg.role as "user" | "assistant",
        content: msg.content,
      })),
      { role: "user", content: message },
    ];

    const response = await client.chat.completions.create({
      model: "gpt-4o-mini",
      messages,
      max_tokens: 400,
      temperature: 0,
      response_format: { type: "json_object" },
    });

    const rawReply = response.choices[0]?.message?.content || '{"reply": "죄송합니다. 답변을 생성하지 못했습니다.", "action": "none", "tabId": null}';

    // JSON 파싱
    let parsedReply;
    try {
      parsedReply = JSON.parse(rawReply);
    } catch {
      console.error("JSON parse error, raw:", rawReply);
      parsedReply = { reply: rawReply, action: "none", tabId: null };
    }

    // 키워드 기반 navigate 폴백: GPT가 none을 반환해도 질문에 섹션 키워드가 있으면 강제 navigate
    if (parsedReply.action === "none" || !parsedReply.action) {
      const msgLower = (message || "").toLowerCase().replace(/\s/g, "");
      // 인사/잡담은 제외
      const greetings = ["안녕", "고마워", "감사", "반가워", "잘가", "바이바이"];
      const isGreeting = greetings.some(g => msgLower.includes(g));

      if (!isGreeting && msgLower.length > 1) {
        let bestMatch: { id: string; score: number } | null = null;
        for (const [sectionId, info] of Object.entries(SECTION_INFO)) {
          for (const kw of info.keywords) {
            const kwNorm = kw.replace(/\s/g, "");
            if (msgLower.includes(kwNorm)) {
              const score = kwNorm.length; // 긴 키워드 우선
              if (!bestMatch || score > bestMatch.score) {
                bestMatch = { id: sectionId, score };
              }
            }
          }
        }
        if (bestMatch) {
          console.log(`🔄 Navigate fallback: "${message}" → ${bestMatch.id} (keyword match)`);
          parsedReply.action = "navigate";
          parsedReply.tabId = bestMatch.id;
        }
      }
    }

    // TTS 발음 후처리 (GPT가 규칙을 안 따를 경우 강제 치환)
    if (parsedReply.reply) {
      parsedReply.reply = parsedReply.reply
        // 학교/학부/학과명
        .replace(/차의과학대학교/g, '차 의과학 대학교')
        .replace(/차의과학대/g, '차의과학대')
        .replace(/헬스케어융합학부/g, '헬스케어 융합 학부')
        .replace(/경영학전공/g, '경영학 전공')
        .replace(/경영학과/g, '경영 학과')
        .replace(/미래융합대학/g, '미래 융합 대학')
        // 병원/기관
        .replace(/차병원그룹/g, '차 병원 그룹')
        .replace(/차병원/g, '차 병원')
        // 과목/분야명 (긴 합성어 띄어쓰기)
        .replace(/기술경영/g, '기술 경영')
        .replace(/조직행동론/g, '조직 행동론')
        .replace(/경영학원론/g, '경영학 원론')
        .replace(/재무관리/g, '재무 관리')
        .replace(/마케팅원론/g, '마케팅 원론')
        .replace(/경영전략론/g, '경영 전략론')
        .replace(/서비스마케팅/g, '서비스 마케팅')
        .replace(/금융시장론/g, '금융 시장론')
        .replace(/소비자심리/g, '소비자 심리')
        .replace(/기업지배구조/g, '기업 지배 구조')
        .replace(/투자자산운용/g, '투자 자산 운용')
        .replace(/캡스톤디자인/g, '캡스톤 디자인')
        .replace(/비즈니스애널리틱스/g, '비즈니스 애널리틱스')
        .replace(/헬스케어비즈니스/g, '헬스케어 비즈니스')
        // 영어 약어 → 한글 발음
        .replace(/\bESG\b/g, '이에스지')
        .replace(/\bAI\b/g, '에이아이')
        .replace(/\bR&D\b/g, '알앤디')
        .replace(/\bIT\b/g, '아이티')
        .replace(/\bIR\b/g, '아이알')
        .replace(/\bPR\b/g, '피알')
        .replace(/\bCR\b/g, '씨알')
        .replace(/\bCRM\b/g, '씨알엠')
        .replace(/\bCDO\b/g, '씨디오')
        .replace(/\bVC\b/g, '브이씨')
        .replace(/\bADsP\b/g, '에이디에스피')
        .replace(/\bSQLD\b/g, '에스큐엘디')
        .replace(/\bRISE\b/g, '라이즈')
        .replace(/\bNGO\b/g, '엔지오')
        .replace(/(\d+)%/g, '$1 퍼센트');
    }

    console.log("🤖 OpenAI response:", parsedReply);

    return new Response(JSON.stringify(parsedReply), {
      status: 200,
      headers: { "Content-Type": "application/json" },
    });
  } catch (error) {
    console.error("API error:", error);
    return new Response(JSON.stringify({ reply: "오류가 발생했습니다.", action: "none", tabId: null }), {
      status: 500,
      headers: { "Content-Type": "application/json" },
    });
  }
}
